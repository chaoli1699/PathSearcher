package cn.cienet.pathsearcher.bean;

public class AimArea {

	private int aimId;
	private String aimName;
	private int pointX;
	private int pointY;
	private String aimDescribe;
	public int getAimId() {
		return aimId;
	}
	public void setAimId(int aimId) {
		this.aimId = aimId;
	}
	public String getAimName() {
		return aimName;
	}
	public void setAimName(String aimName) {
		this.aimName = aimName;
	}
	public int getPointX() {
		return pointX;
	}
	public void setPointX(int pointX) {
		this.pointX = pointX;
	}
	public int getPointY() {
		return pointY;
	}
	public void setPointY(int pointY) {
		this.pointY = pointY;
	}
	public String getAimDescribe() {
		return aimDescribe;
	}
	public void setAimDescribe(String aimDescribe) {
		this.aimDescribe = aimDescribe;
	}
	
}
